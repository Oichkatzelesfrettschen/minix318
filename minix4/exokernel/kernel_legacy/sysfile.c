int
sys_fcntl(void)
{
  int fd, cmd, arg;
  struct file *f;

  if(argfd(0, &fd, &f) < 0 || argint(1, &cmd) < 0)
    return -1;
  if(argint(2, &arg) < 0)
    arg = 0;

  switch(cmd){
  case F_GETFL:
    return f->flags;
  case F_SETFL:
    f->flags = arg;
    return 0;
  default:
    return -1;
  }
}
